public class unary {
    public static void main(String [] args){
//        post increment
        int a = 5;
        int b = 0;
        b = a++;
        System.out.println(a);
        System.out.println(b);
//preincrement
        int x = 5;
        int y = 0;

        y = ++x;
        System.out.println(x);
        System.out.println(y);
    }
}
